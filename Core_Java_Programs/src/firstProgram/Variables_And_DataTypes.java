package firstProgram;

public class Variables_And_DataTypes {
	public static void main(String[] args) {
		int num = 10;
		double decimal = 3.145;
		char character = 'A';
		String text = "Hello";
		boolean isTrue = true;

		System.out.println("=====Printing Variables======");
		System.out.println("Integer: " + num + "\n" + "Decimal: " + decimal + "\n" + "Character: " + character + "\n"
				+ "String: " + text + "\n" + "Boolean: " + isTrue);
	}

}
